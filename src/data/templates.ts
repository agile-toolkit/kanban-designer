export function cloneTemplate(t: typeof TEMPLATES[0]): KanbanBoard {
  return {
    id: crypto.randomUUID(),
    name: t.name,
    columns: t.columns.map(c => ({ ...c, id: crypto.randomUUID(), cards: [] })),
    swimLanes: [...t.swimLanes],
    showWipWarnings: t.showWipWarnings,
  }
}
